
public class Attendance {
	
	private String asuRite;
	private int time;
	
	public Attendance(String asuRite, int time) {
		this.asuRite = asuRite;
		this.time = time;
	}
	
	public String getAsuRite() {
		return asuRite;
	}
	public void setAsuRite(String asuRite) {
		this.asuRite = asuRite;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
	

}
